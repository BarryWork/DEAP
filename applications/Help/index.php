            Getting Started
          </div>
          <div class="card-body">
            <p>Start to get familiar with the ABCD way of naming variables by using the Explore application. Use the hierarchical view to get an overview by domains and the search field for a free-text search. You will notice that the variable names usually start with the name of the instrument used to capture the values. You can find similar variables by changing the end of the search term.</p>
      <p>Next use the Analysis application, press the "Submit" button. Click on "Compact Mode" in the menu and read up on the generated graphs. Try to add your own variables as dependent or independent variables.</p>
            <p>Once you are familiar with this general workflow read up on the <i>Limit</i> and <i>Extend</i> applications to tailor your analysis.</p>
          </div>
        </div>
        
        <div class = "card">
          <div class="card-header">
            About the Study
          </div>
          <div class="card-body">
            <p>The Data Exploration and Analysis Portal (DEAP) provides advanced statistical analysis functions that we built to work with the Adolescent Brain Cognitive Development (ABCD) Study - a long-term study of cognitive and brain development in children across the United States. From 2016-2018, we invited children between the ages of 9-11 to join ABCD from 20 sites around the nation, with the intent to enroll and follow 11,900 healthy children longitudinally for 10 years into young adulthood. </p>
          </div>
        </div>
        
        <div class = "card">
          <div class="card-header">
            About the Data
          </div>
          <div class="card-body">
            <p>The data available on DEAP is a copy of the published data of the ABCD study on the National Data Archive (NDA). The data is pre-processed and merged into a single large database that is easiest understood as a spreadsheet. This process is documented on a public repository at <a href="https://github.com/ABCD-STUDY/analysis-nda17">github.com/ABCD-STUDY/analysis-nda17</a>. The current version of DEAP is based on the <i>ABCD Release 1.1</i> package under the doi: 10.15154/1460410.</p>
            <p>Access to DEAP is only available to users with an existing NDA data use certification (<a href="https://data-archive.nimh.nih.gov/abcd">apply here</a>).</p>
          </div>
        </div>
        
        <div class = "card">
          <div class="card-header">
            Overview
          </div>
          <div class="card-body">
            <p>DEAP provides the means to find measures of interest from more than 40,000 shared observations available for each ABCD participant (see Explore). The user can select a sub-set of the participants for a hypothesis test using a multi-level regression model suitable for the ABCD study design (see Limit). Users are able to extend DEAP by their own measures (see Extend) and run novel multi-level statistical analysis directly from the DEAP interface (see Analyse).</p>
            <p>Do you think you have found an error in DEAP? Help us to improve the software by reporting it on <a href="https://github.com/ABCD-STUDY/DEAP/issues">https://github.com/ABCD-STUDY/DEAP/issues</a>. Let us know how we can reproduce your bug and share a screenshot.</p>
          </div>
        </div>

        <div class = "card">
          <div class="card-header">
            Plan
          </div>
          <div class="card-body">
            <p>We support a hypothesis driven workflow on DEAP by providing material for registering your hypothesis prior to the full analysis. This includes a sampling plan, design plan, analysis plan and analysis scripts. Please see our detailed information in the <i>Plan</i> application.</p>
            <button class="btn btn-light" video="https://youtu.be/VA76sx_uWYE">Video</button>
          </div>
        </div>

        <div class = "card">
          <div class="card-header">
            Explore
          </div>
          <div class="card-body">
            <p>The <i>Explore</i> application provides an overview of the existing measures on DEAP. Two options for searching the data dictionaries of DEAP exist. A full-text search can be done given some keywords that can be in either the name or in the description of the measure. The second option is to use a graphical tree-view that lists the different domains. Click on a domain and sub-structures are displayed that end with a full list of the measure names.</p>
            <button class="btn btn-light" video="https://youtu.be/eTxW5ctb-vA">Video</button>
          </div>
        </div>

        <div class = "card">
          <div class="card-header">
            Analyze
          </div>
          <div class="card-body">
            <p>The statistical analysis package used by DEAP is based on the R-package <a href="https://cran.r-project.org/web/packages/gamm4/"><i>gamm4</i></a> and implements a generalized additive mixel model that captures the degrees of freedom in a longitudinal multi-site study that includes a complex family structure. The user interface of the analysis application provides meaningful presets for covariates of no interest and in general the user is only required to change the variables of interest, the dependent and independent variable to start the analysis.</p>
            <p>Learn how to load and save a model (see the menu button on the top left).</p>
            <p>More extensive help is available in the application itself. The menu option <i>Normal</i> can be switched to <i>Tutorial</i> mode which shows text on how to interpret the generated graphs and tables.</p>
            <button class="btn btn-light" video="https://youtu.be/BXzuJ6OE0QA">Video</button>
          </div>
        </div>

        <div class = "card">
          <div class="card-header">
            Limit
          </div>
          <div class="card-body">
            <p>The statistical analysis in the Analyze application by default applies to all participants with data. In order to limit the analysis to a subset of participants the <i>Limit</i> application is used to define the list of participants. These named lists are saved and can be selected in the Analysis application.</p>
            <p>Help on the syntax of the subsetting is available in the Limit application.</p>
            <button class="btn btn-light" video="https://youtu.be/6Mle4STc3dU">Video</button>
          </div>
        </div>

        <div class = "card">
          <div class="card-header">
            Extend
          </div>
          <div class="card-body">
            <p>If you want to add measures to DEAP you can do this using the <i>Extend</i> application. It is not possible to add new participants, but you can change and combine existing measures and use them during the analysis.</p>
            <button class="btn btn-light" video="https://youtu.be/EhjO3Zu2T9c">Video</button>
          </div>
